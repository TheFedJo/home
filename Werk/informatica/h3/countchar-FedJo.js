//countBs

function countBs(Bstring) {
  let counter = 0;
  let countB = 0;
  
  while (counter <= Bstring.length) {
    if (Bstring[counter] === "B") {
      counter++;
      countB++;
    } else {
      counter++;
    };
    
  
  };
  return countB;
};


