function countchar(Bstring, char = "B") {
  let counter = 0;
  let countchar = 0;
  
  while (counter <= Bstring.length) {
    if (Bstring[counter] === char) {
      counter++;
      countchar++;
    } else {
      counter++;
    };
    
  
  };
  return countchar;
};

